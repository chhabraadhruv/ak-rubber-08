// Enhanced helper functions with SEO considerations
export const getProductsByCategory = (category: string): Product[] => {
  return products.filter(product => product.category === category);
};

export const getAllCategories = (): string[] => {
  const categories = products.map(product => product.category).filter(Boolean) as string[];
  return [...new Set(categories)];
};

// SEO helper functions
export const getProductsByLocation = (location: string): Product[] => {
  return products.filter(product => 
    product.location?.toLowerCase().includes(location.toLowerCase())
  );
};

export const getProductsByKeyword = (keyword: string): Product[] => {
  return products.filter(product => 
    product.keywords?.some(k => k.toLowerCase().includes(keyword.toLowerCase())) ||
    product.name.toLowerCase().includes(keyword.toLowerCase()) ||
    product.description.toLowerCase().includes(keyword.toLowerCase())
  );
};

export const getFeaturedProducts = (): Product[] => {
  // Return top products for SEO landing pages
  return products.slice(0, 4);
};

